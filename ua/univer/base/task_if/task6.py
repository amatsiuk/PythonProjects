def count_positive_value(a,b,c):
    count=0
    if a>0:
        count+=1
    if b>0:
        count+=1
    if c>0:
        count+=1
    print("count = ", count)

def count_negative_value(a,b,c):
    count=0
    if a<0:
        count+=1
    if b<0:
        count+=1
    if c<0:
        count+=1
    print("count = ", count)



